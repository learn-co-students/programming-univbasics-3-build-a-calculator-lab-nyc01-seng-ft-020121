def addition(num1, num2)
sum = num2 + num1
return sum
end

def subtraction(num1, num2)
delta = num1 - num2
return delta
end

def division(num1, num2)
div = num1 / num2
end

def multiplication(num1, num2)
times = num2 * num1
return times
end

def modulo(num1, num2)
leftover = num1 % num2
return leftover
end

def square_root(num)
root = Math.sqrt(num)
return root
end